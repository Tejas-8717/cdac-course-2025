package com.ass1;

import java.util.Scanner;

public class Pattern {

	public static void main(String[] args) {
		int row=5;
		
		for(int i=1;i<=row;i++);{
		{
			for(int space=row-1;space>0;space--) {
				System.out.println("");
			}
			for(int star=1;star<=1;star++) {
				System.out.println("* ");
			}
			System.out.println();
		}
	}
	}}
